<h1>About this app</h1>

<p>This is a simple Hacker News clone, built with <a href="https://sapper.svelte.dev">Sapper</a>, an application framework for <a href="https://svelte.dev">Svelte</a>. It is based upon the sample HackerNews client published by Svelte themselves available [here](https://github.com/sveltejs/hn.svelte.dev) with the addition of some Navs and ProductHunt and dev.to News Feeds.</p>

